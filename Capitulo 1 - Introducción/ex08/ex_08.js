// Funciones de tipo STRING

let palabra = "JavaScript";

document.writeln('La palabra es: ', palabra,' <br>');
console.log('La palabra es: ', palabra);

document.writeln(palabra.toUpperCase(),' <br>'); // Convertir la palabra en mayúscula
document.writeln(palabra.toLowerCase(),' <br>'); // Convertir la palabra en minúscula

let textoUno, textoDos;

textoUno = 'Curso de ';
textoDos = 'Fundamentos de JavaScript.';

document.writeln(textoUno.concat(textoDos),' <br>'); // Unión de 2 o más variables

document.writeln(palabra.length); // Contador de carácteres en la palabra
console.log('Palabra escita: ', palabra);

